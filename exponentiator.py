def exponentiate(a, b):
    '''
    :param a: first number
    :param b: second number
    :return: first number to the second number powered
    '''
    return a ** b


def raise_to_fourth_power(a):
    '''
    :param a: number
    :return: number to the 4th power
    '''
    return a ** 4


def square(a):
    '''
    :param a: number
    :return: number squared
    '''
    return a * a


def cube(a):
    '''
    :param a: number
    :return: number cubed
    '''
    return a ** 3

print(square(2))
print(cube(2))
print(raise_to_fourth_power(2))